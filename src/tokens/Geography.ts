/**
 * List of spacing options
 */
export enum Spacing {
  /**
   * Arms length spacing for vertical and horizontal element groups
   */
  Wide = "wide",

  /**
   * Shoulder spacing for vertical and horizontal element groups
   */
  Tight = "tight",

  /**
   * Elbow room spacing for vertical and horizontal element groups
   */
  Cozy = "cozy",
}
